package Curso_Java_Thiago_Aguiar.Arrays.Introducao_Programacao_Java;

public class Tipos_Primitivos {

    public static void main(String[] args) {

        //Tipos Primitivos

        boolean isLogged = true;
        boolean isLogout = false;

        byte b = 'b';
        char c = 'c';

        short s = -129;   // semelhante ao int só que com menos espaço, por isso short
        int o = 10;

        // Perceba que o long tem que SEMPRE ter o "L" maiusculo no final
        long a = 111213211L;    // semelhante ao int só que com mais espaço, por isso long

        // Perceba que o float tem que SEMPRE ter o "f" no final
        float t = 10.2f;      // semelhante ao double só que menor, com 32 bits
        double v = 1000000;   // semelhante ao float só que maior, com 64 bits

        //Tipos Não Primitivos

        String caracter = "String";

    }

}
